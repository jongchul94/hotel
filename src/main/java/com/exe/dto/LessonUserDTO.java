package com.exe.dto;

public class LessonUserDTO {
	
	private int lessonUserIndex;
	private String lessonUserId;
	private String lessonUserName;
	private String lessonUserEmail;
	private String lessonUserDay;
	private String className;
	
	public int getLessonUserIndex() {
		return lessonUserIndex;
	}
	public void setLessonUserIndex(int lessonUserIndex) {
		this.lessonUserIndex = lessonUserIndex;
	}
	public String getLessonUserId() {
		return lessonUserId;
	}
	public void setLessonUserId(String lessonUserId) {
		this.lessonUserId = lessonUserId;
	}
	public String getLessonUserName() {
		return lessonUserName;
	}
	public void setLessonUserName(String lessonUserName) {
		this.lessonUserName = lessonUserName;
	}
	public String getLessonUserEmail() {
		return lessonUserEmail;
	}
	public void setLessonUserEmail(String lessonUserEmail) {
		this.lessonUserEmail = lessonUserEmail;
	}
	public String getLessonUserDay() {
		return lessonUserDay;
	}
	public void setLessonUserDay(String lessonUserDay) {
		this.lessonUserDay = lessonUserDay;
	}
	public String getClassName() {
		return className;
	}
	public void setClassName(String className) {
		this.className = className;
	}

}